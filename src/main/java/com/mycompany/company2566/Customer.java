    private void cusCodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cusCodeKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            try {
                String selectQuery = "SELECT * FROM customer where customerCode=" + cusCode.getText();
                ResultSet resultSet = statement.executeQuery(selectQuery);
                while (resultSet.next()) {
                    cusName.setText(resultSet.getString(2));
                    cusAddress.setText(resultSet.getString(3));
                    cusTel.setText(resultSet.getString(4));
                    TotalSale.setText(resultSet.getString(5));
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_cusCodeKeyPressed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        int pressed = JOptionPane.showConfirmDialog(null, "ตกลง", "ลบข้อมูล ?", JOptionPane.OK_CANCEL_OPTION);
        if (pressed == 0) {
            String Ccode = cusCode.getText();
            try {
                String deleteQuery = "DELETE FROM customer WHERE customerCode = ?";
                PreparedStatement preparedStatement = conn.prepareStatement(deleteQuery);
                preparedStatement.setString(1, Ccode);
                int rowsAffected = preparedStatement.executeUpdate();
                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(this, "ลบข้อมูลเรียบร้อยแล้ว.", "แจ้งเตือน", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "ลบข้อมูลไม่สำเร็จ", "แจ้งเตือน", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
            newButton.doClick();
            displayCustomerData();
        }
        newButton.doClick();
        displayCustomerData();

    }//GEN-LAST:event_deleteActionPerformed

