    private void showButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showButtonActionPerformed
        try {
            String query = "SELECT * from customer order by customerCode asc";

            ResultSet resultSet = statement.executeQuery(query);
            StringBuilder message = new StringBuilder("ข้อมูลลูกค้า\n");
            while (resultSet.next()) {
                int Ccode = resultSet.getInt("customerCode");
                String Cname = resultSet.getString("customerName");
                String Caddress = resultSet.getString("address");
                int Ctel = resultSet.getInt("tel");
                int Ctotal = resultSet.getInt("totalSale");

                message.append("--------------------------------------------------- ").append("\n");
                message.append("รหัสลูกค้า : ").append(Ccode).append("\n");
                message.append("ชื่อลูกค้า :").append(Cname).append("\n");
                message.append("ที่อยู่ :").append(Caddress).append("\n");
                message.append("เบอร์โทร :").append(Ctel).append("\n");
                message.append("ยอดขายรวม :").append(Ctotal).append("\n");
            }

            JTextArea textArea = new JTextArea(message.toString());
            textArea.setFont(new Font("Microsoft Sans Serif", Font.PLAIN, 14)); // Set the font here
            textArea.setEditable(false);
            JScrollPane scrollPane = new JScrollPane(textArea);
            scrollPane.setPreferredSize(new Dimension(400, 300));
            JOptionPane.showMessageDialog(this, scrollPane, "ข้อมูลลูกค้า", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error retrieving item data.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_showButtonActionPerformed

